// PROGRAMMING TO PREDICT THE OUTPUT
#include<stdio.h>
int main(){
    
    int p,q;
    printf("Enter the value of p and q :\n");
    scanf("%d %d",&p,&q); // & is for storing value into memory address
    
    printf("The value of p is : %d\n",p);
    printf("The value of q is : %d\n",q);
    
    return 0;
}