// UPDATION OF VARIABLES ONE BY ONE RESPECTIVELY
#include<stdio.h>
int main(){
    int x;
    x = 18;
    printf("%d",x);
    printf("\n");

    x = 45;
    printf("%d",x);
    printf("\n");

    x = x + 100;
    printf("%d",x);
    printf("\n");

    x = x - 14;
    printf("%d",x);
    printf("\n");
    return 0;
}