// PRINTING MULTIPLE INTEGERS TOGETHER ONE BY ONE
#include<stdio.h>
int main(){
    int x;
    x = 18;
    printf("VIRAT KOHLI--JERSEY NO.");
    printf("%d\n",x);
    printf("\n");

    x = 45;
    printf("ROHIT SHARMA--JERSEY NO.");
    printf("%d\n",x);
    printf("\n");

    x = 1;
    printf("KL RAHUL--JERSEY NO.");
    printf("%d\n",x);
    printf("\n");
    return 0;
}